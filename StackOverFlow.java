class StackOverFlow
{
	public static void main(String[] args)
	{
		System.out.println("Main Starts");
		ramesh();
		System.out.println("Ramesh Starts");
	}
	public static void ramesh()
	{
		System.out.println("Ramesh Starts");
		String [] list = {"Anup","Ritu","Mukesh","Awadh"};
		main(list);
		System.out.println("Ramesh Ends");
	}
}